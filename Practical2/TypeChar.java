public class TypeChar {
    public static void main(String[] args) {
        char symb1=1067;
        char symb2 ='Ð«';
        System.out.println("symb1 contains "+ symb1);
        System.out.println("symb2 contains "+ symb2);

        char ch = 'J';
        int intCh = (int) ch;
        System.out.println("J corresponds with "+ intCh);
    }
}
